#include "../include/ft_printf.h"

int	ft_isformat_bonus(char c)
{
	if (c == 'i' || c == 'c' || c == 's' || c == 'x' || c == 'X' || c == 'u'
		|| c == 'd' || c == '%' || c == 'p')
		return (1);
	return (0);
}
